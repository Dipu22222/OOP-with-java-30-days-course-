package Day5;

public class MathOperations {
    //method to add two integer
    int add(int a, int b) {
        return a+b;
    }
    //method to add three integer
    int add(int a,int b, int c) {
        return a + b + c;
    }
    //method to add two double variable
     double add(double a, double b) {
        return a + b;
     }
}
