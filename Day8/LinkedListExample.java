package Day8;


import java.util.LinkedList;
public class LinkedListExample {
    public static void main(String[] args) {
        // Creating a linklist
        LinkedList<String> Animal = new LinkedList<>();
        // Adding elements
        Animal.add("Cow");
        Animal.add("Cat");
        Animal.add("GOAT");
        // Displaying the elements
        System.out.println("The Animals: " + Animal);

    }
}
